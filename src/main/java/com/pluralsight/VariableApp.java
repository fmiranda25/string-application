package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {

        String favColor = "blue";
        System.out.println(favColor);

        int yearUpStart = 2026;
        System.out.println(yearUpStart);

        String midInit = " ";
        System.out.println(midInit);

        boolean hasPet = true;
        System.out.println(hasPet);

        String greeting =  "Top of the morning to ya lads!";
        System.out.println(greeting);

        byte daysInTheWeek = 7;
        System.out.println(daysInTheWeek);

        float coffeePrice = 4.99F;
        System.out.println(coffeePrice);

        char favLetter = 'E';
        System.out.println(favLetter);

        boolean isRaining = false;
        System.out.println(isRaining);

        System.out.println("My favorite color is " + favColor + ".");
        System.out.println("I started the Year Up program in " + yearUpStart + ".");
        System.out.println("My middle initial is: " + midInit + "because I don't have one.");
        System.out.println("Based on the amount of cats that live in my house, do I have pets? " + hasPet + ".");
        System.out.println("There are " + daysInTheWeek + " days in the week!");
        System.out.println("The price of coffee is pretty decent, only $" + coffeePrice + "!");
        System.out.println("My favorite letter is the letter " + favLetter + ".");
        System.out.println("Is it raining today? Nah " + isRaining + " it isn't.");


    }
}
